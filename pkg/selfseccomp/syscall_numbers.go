package selfseccomp

// baselineSyscallNumbers maps syscall names to NRs for the given
// arch. Numbers come from <asm/unistd_64.h> (amd64) and the arm64
// equivalent. We keep them as a static map rather than pulling in
// libseccomp because xhelix is CGO_ENABLED=0.
//
// Unknown names are silently skipped — the caller (audit mode)
// will see them appear in the audit log if the kernel rejects the
// syscall.
//
// References:
//   - amd64: kernel arch/x86/include/generated/uapi/asm/unistd_64.h
//   - arm64: kernel arch/arm64/include/generated/uapi/asm/unistd.h
//   - golang.org/x/sys/unix has SYS_* constants per arch; this list
//     is hand-maintained because the syscall set is smaller than
//     unix.SYS_*.
func numberTableForArch(arch string) map[string]uint32 {
	switch arch {
	case "amd64", "x86_64":
		return amd64Numbers
	case "arm64", "aarch64":
		return arm64Numbers
	}
	return nil
}

